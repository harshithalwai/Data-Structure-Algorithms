package tool;
class Hybrid_calculator {
    void add(int x, int y) {
        System.out.println(x + y);
    }

    public void sub(int x, int y) {
        System.out.println(x - y);
    }

    protected void mul(int x, int y) {
        System.out.println(x * y);
    }

    private void div(int x, int y) {
        System.out.println(x / y);
    }
}

